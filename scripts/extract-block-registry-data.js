function extractArray(src, varName) {
  // Find `const <varName>: ... = [` and match until balanced closing `];`
  const startIdx = src.indexOf(`const ${varName}`);
  if (startIdx === -1) return null;
  const eqIdx = src.indexOf('=', startIdx);
  if (eqIdx === -1) return null;
  const bracketIdx = src.indexOf('[', eqIdx);
  if (bracketIdx === -1) return null;
  let depth = 0;
  let i = bracketIdx;
  for (; i < src.length; i++) {
    if (src[i] === '[') depth++;
    else if (src[i] === ']') {
      depth--;
      if (depth === 0) { i++; break; }
    }
  }
  // Return inner content (without outer `[` / `]`) so we can concat
  return src.slice(bracketIdx + 1, i - 1).trim();
}

function stripPreviewField(src) {
  // Remove `preview: <anything>` property from object literals by balancing
  // brackets/parens/strings until we hit the next top-level comma (relative to
  // where preview started) or the block's closing `}`.
  let out = '';
  let i = 0;
  while (i < src.length) {
    const keyMatch = src.slice(i).match(/^preview\s*:\s*/);
    if (!keyMatch) { out += src[i++]; continue; }
    // Skip past `preview:` and consume the value expression
    i += keyMatch[0].length;
    let depth = 0;
    let inStr = null;
    while (i < src.length) {
      const c = src[i];
      if (inStr) {
        if (c === '\\') { i += 2; continue; }
        if (c === inStr) inStr = null;
        i++;
        continue;
      }
      if (c === '"' || c === "'" || c === '`') { inStr = c; i++; continue; }
      if (c === '{' || c === '(' || c === '[') { depth++; i++; continue; }
      if (c === '}' || c === ')' || c === ']') {
        if (depth === 0) break; // end of outer block literal — leave `}` in place
        depth--;
        i++;
        continue;
      }
      if (c === ',' && depth === 0) { i++; break; } // swallow trailing comma
      i++;
    }
    // Swallow any trailing whitespace after the removed property
    while (i < src.length && /\s/.test(src[i])) { out += src[i++]; }
  }
  return out;
}

const entries = [];
const subs = [];

for (const v of VERTICALS) {
  const files = ['index.tsx', 'index.ts'];
  let src = null;
  for (const f of files) {
    const p = path.join(PREVIEWS_DIR, v.dir, f);
    if (fs.existsSync(p)) { src = fs.readFileSync(p, 'utf8'); break; }
  }
  if (!src) { console.error(`Missing file: ${v.dir}`); continue; }

  const blocksRaw = extractArray(src, `${v.prefix}_BLOCKS`);
  const subsRaw = extractArray(src, `${v.prefix}_SUBVERTICALS`);
  if (!blocksRaw) { console.error(`No _BLOCKS in ${v.dir}`); continue; }
  if (!subsRaw) { console.error(`No _SUBVERTICALS in ${v.dir}`); continue; }

  const stripTrailingComma = (s) => s.replace(/,\s*$/, '');
  entries.push(`  // ── ${v.dir} ──\n${stripTrailingComma(stripPreviewField(blocksRaw))}`);
  subs.push(`  // ── ${v.dir} ──\n${stripTrailingComma(subsRaw)}`);
}


export function generateSystemPrompt(assistant: Partial<Assistant>): string {
    const { name, description, personality, behaviorRules } = assistant;

    const toneDescription = personality?.tone ? `Your tone is ${personality.tone}.` : '';
    const styleDescription = personality?.style ? `Your communication style is ${personality.style}.` : '';
    const lengthDescription = personality?.responseLength
        ? `Keep responses ${personality.responseLength === 'brief' ? 'brief (1-2 sentences)' : personality.responseLength === 'detailed' ? 'detailed (3-5 sentences)' : 'moderate (2-3 sentences)'}.`
        : '';

    const rulesSection = behaviorRules?.responseRules?.length
        ? `\n\nRULES TO FOLLOW:\n${behaviorRules.responseRules.map(r => `- ${r}`).join('\n')}`
        : '';

    const neverSection = behaviorRules?.neverSay?.length
        ? `\n\nNEVER SAY:\n${behaviorRules.neverSay.map(r => `- "${r}"`).join('\n')}`
        : '';

    const escalationSection = behaviorRules?.escalationTriggers?.length
        ? `\n\nESCALATE TO HUMAN WHEN:\n${behaviorRules.escalationTriggers.map(r => `- ${r}`).join('\n')}`
        : '';

    return `You are ${name || 'an AI assistant'}. ${description || ''}

${toneDescription} ${styleDescription} ${lengthDescription}
${rulesSection}${neverSection}${escalationSection}

Always be helpful, accurate, and respectful. Use the provided knowledge base to answer questions.`.trim();
}

export function createAssistantFromTemplate(
    template: AssistantTemplate,
    partnerId: string,
    createdBy: string
): Omit<Assistant, 'createdAt' | 'updatedAt'> {
    const systemPrompt = generateSystemPrompt({
        name: template.name,
        description: template.description,
        personality: template.personality,
        behaviorRules: template.behaviorRules as AssistantBehaviorRules,
    });

    return {
        id: `custom-${Date.now()}-${Math.random().toString(36).substr(2, 9)}`,
        partnerId,
        type: 'custom',
        templateId: template.id,
        name: template.name,
        description: template.description,
        avatar: template.avatar,
        color: template.color,
        personality: template.personality,
        documentConfig: {
            useAllDocuments: true,
            attachedDocumentIds: [],
            externalDocumentIds: [],
        },
        behaviorRules: {
            responseRules: template.behaviorRules.responseRules || [],
            neverSay: template.behaviorRules.neverSay || [],
            escalationTriggers: template.behaviorRules.escalationTriggers || [],
            openingMessage: template.behaviorRules.openingMessage || '',
        },
        systemPrompt,
        systemPromptAutoGenerated: true,
        allowExternalUse: true,
        isActive: true,
        temperature: 0.7,
        maxTokens: 1024,
        createdBy,
        usageCount: 0,
    };
}


export type AssistantType = 'essential' | 'custom';
export type AssistantTone = 'professional' | 'friendly' | 'casual' | 'formal' | 'empathetic' | 'consultative';
export type AssistantStyle = 'formal' | 'conversational' | 'casual';
export type AssistantResponseLength = 'brief' | 'moderate' | 'detailed';
export type DocumentVisibility = 'internal' | 'external' | 'both';

export interface AssistantPersonality {
    tone: AssistantTone;
    style: AssistantStyle;
    responseLength: AssistantResponseLength;
}

export interface AssistantDocumentConfig {
    useAllDocuments: boolean;
    attachedDocumentIds: string[];
    externalDocumentIds: string[];
}

export interface AssistantBehaviorRules {
    responseRules: string[];
    neverSay: string[];
    escalationTriggers: string[];
    openingMessage?: string;
}

export interface Assistant {
    id: string;
    partnerId: string;
    type: AssistantType;
    templateId?: string;

    name: string;
    description: string;
    avatar: string;
    color: string;

    personality: AssistantPersonality;

    documentConfig: AssistantDocumentConfig;

    behaviorRules: AssistantBehaviorRules;

    systemPrompt: string;
    systemPromptAutoGenerated: boolean;
    systemPromptManualOverride?: string;

    allowExternalUse: boolean;

    isActive: boolean;
    temperature: number;
    maxTokens: number;

    createdAt: Timestamp | Date;
    updatedAt: Timestamp | Date;
    createdBy?: string;
    usageCount: number;
}


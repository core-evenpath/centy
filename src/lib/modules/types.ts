// ============================================================================
// FIELD & CATEGORY DEFINITIONS
// ============================================================================

export type ModuleFieldType =
    | 'text'
    | 'number'
    | 'currency'
    | 'select'
    | 'multi_select'
    | 'toggle'
    | 'tags'
    | 'textarea'
    | 'image'
    | 'date'
    | 'time'
    | 'duration'
    | 'url'
    | 'email'
    | 'phone';

export interface ModuleFieldDefinition {
    id: string;
    name: string;
    type: ModuleFieldType;
    description?: string;
    isRequired: boolean;
    isSearchable: boolean;
    showInList: boolean;
    showInCard: boolean;
    placeholder?: string;
    options?: string[];
    defaultValue?: any;
    validation?: {
        min?: number;
        max?: number;
        minLength?: number;
        maxLength?: number;
        pattern?: string;
        patternMessage?: string;
    };
    conditionalOn?: {
        fieldId: string;
        value: any;
    };
    order: number;
}

export interface ModuleCategoryDefinition {
    id: string;
    name: string;
    icon?: string;
    description?: string;
    color?: string;
    order: number;
}

export interface ModuleSchema {
    fields: ModuleFieldDefinition[];
    categories: ModuleCategoryDefinition[];
}

// ============================================================================
// SCHEMA VERSIONING
// ============================================================================

export interface ModuleSchemaVersion {
    version: number;
    fields: ModuleFieldDefinition[];
    categories: ModuleCategoryDefinition[];
    generatedAt: string;
    generatedBy: 'ai' | 'manual';
    aiModel?: string;
    aiPromptHash?: string;
    status: 'active' | 'deprecated' | 'sunset';
    sunsetDate?: string;
    changeNotes?: string;
}

export interface ModuleFieldMapping {
    oldFieldId: string;
    newFieldId: string;
    transform?: 'direct' | 'rename' | 'convert';
    convertFn?: string;
}

export interface ModuleMigration {
    id: string;
    fromVersion: number;
    toVersion: number;
    fieldMappings: ModuleFieldMapping[];
    defaultValues: Record<string, any>;
    removedFields: string[];
    addedFields: string[];
    createdAt: string;
    createdBy: string;
    isAutoGenerated: boolean;
}

// ============================================================================
// SYSTEM MODULE (Admin-managed template)

export type ModulePriceType = 'one_time' | 'per_night' | 'per_hour' | 'per_session' | 'per_day' | 'per_week' | 'per_month' | 'per_year' | 'custom';

export interface SystemModuleSettings {
    allowCustomFields: boolean;
    allowCustomCategories: boolean;
    maxItems: number;
    maxCustomFields: number;
    maxCustomCategories: number;
    requiresPrice: boolean;
    requiresImage: boolean;
    requiresCategory: boolean;
    enableVariants: boolean;
    enableInventoryTracking: boolean;
    minSchemaVersion: number;
}

export interface SystemModule {
    id: string;
    slug: string;
    name: string;
    description: string;
    icon: string;
    color: string;

    currentVersion: number;
    schema: ModuleSchema;
    schemaHistory: Record<number, ModuleSchemaVersion>;
    migrations: Record<string, ModuleMigration>;

    itemLabel: string;
    itemLabelPlural: string;
    priceLabel: string;
    priceType: ModulePriceType;
    defaultCurrency: string;

    settings: SystemModuleSettings;

    applicableIndustries: string[];
    applicableFunctions: string[];

    status: 'draft' | 'active' | 'deprecated';

    usageCount: number;
    lastUsedAt?: string;

    createdAt: string;
    updatedAt: string;
    createdBy: string;
}


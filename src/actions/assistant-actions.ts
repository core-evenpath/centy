"use server";

import { db } from '@/lib/firebase-admin';
import { Timestamp } from 'firebase-admin/firestore';
import {
    Assistant,
    AssistantTemplate,
    ASSISTANT_TEMPLATES,
    ESSENTIAL_ASSISTANT_DEFAULTS,
    generateSystemPrompt,
    createAssistantFromTemplate,
} from '@/lib/types-assistant';

const serializeAssistant = (data: any, id: string): Assistant => {
    return {
        ...data,
        id,
        createdAt: data.createdAt?.toDate ? data.createdAt.toDate() : (data.createdAt instanceof Date ? data.createdAt : new Date(data.createdAt || Date.now())),
        updatedAt: data.updatedAt?.toDate ? data.updatedAt.toDate() : (data.updatedAt instanceof Date ? data.updatedAt : new Date(data.updatedAt || Date.now())),
    } as Assistant;
};

export async function getAssistantsAction(
    partnerId: string
): Promise<{ success: boolean; assistants?: Assistant[]; error?: string }> {
    try {
        if (!db) {
            return { success: false, error: 'Database unavailable' };
        }

        const snapshot = await db
            .collection('partners')
            .doc(partnerId)
            .collection('hubAgents')
            .orderBy('createdAt', 'desc')
            .get();

        const customAssistants: Assistant[] = snapshot.docs.map(doc =>
            serializeAssistant(doc.data(), doc.id)
        );

        const essentialIds = customAssistants
            .filter(a => a.type === 'essential')
            .map(a => a.id);

        const missingEssentials = ESSENTIAL_ASSISTANT_DEFAULTS
            .filter(e => !essentialIds.includes(e.id!))
            .map(e => ({
                ...e,
                partnerId,
                documentConfig: e.documentConfig || {
                    useAllDocuments: e.id !== 'essential-general_mode',
                    attachedDocumentIds: [],
                    externalDocumentIds: [],
                },
                systemPromptAutoGenerated: true,
                isActive: true,
                createdAt: new Date(),
                updatedAt: new Date(),
                usageCount: 0,
            })) as Assistant[];

        const allAssistants = [...missingEssentials, ...customAssistants];

        return { success: true, assistants: allAssistants };
    } catch (error: any) {
        console.error('Get assistants error:', error);
        return { success: false, error: error.message };
    }
}

export async function createAssistantAction(
    partnerId: string,
    data: Omit<Assistant, 'id' | 'partnerId' | 'createdAt' | 'updatedAt' | 'usageCount'>,
    createdBy: string
): Promise<{ success: boolean; assistantId?: string; error?: string }> {
    try {
        if (!db) {
            return { success: false, error: 'Database unavailable' };
        }

        const assistantId = `custom-${Date.now()}-${Math.random().toString(36).substr(2, 9)}`;

        const assistantData: Omit<Assistant, 'id'> & { id: string } = {
            ...data,
            id: assistantId,
            partnerId,
            type: 'custom',
            createdAt: Timestamp.now(),
            updatedAt: Timestamp.now(),
            createdBy,
            usageCount: 0,
        };

        await db
            .collection('partners')
            .doc(partnerId)
            .collection('hubAgents')
            .doc(assistantId)
            .set(assistantData);

        console.log(`✅ Assistant created: ${assistantId}`);

        return { success: true, assistantId };
    } catch (error: any) {
        console.error('Create assistant error:', error);
        return { success: false, error: error.message };
    }
}

export async function createAssistantFromTemplateAction(
    partnerId: string,
    templateId: string,
    createdBy: string
): Promise<{ success: boolean; assistantId?: string; assistant?: Assistant; error?: string }> {
    try {
        if (!db) {
            return { success: false, error: 'Database unavailable' };
        }

        const template = ASSISTANT_TEMPLATES.find(t => t.id === templateId);
        if (!template) {
            return { success: false, error: 'Template not found' };
        }

        const assistantData = createAssistantFromTemplate(template, partnerId, createdBy);

        const fullAssistant: Assistant = {
            ...assistantData,
            createdAt: Timestamp.now() as any,
            updatedAt: Timestamp.now() as any,
        };

        await db
            .collection('partners')
            .doc(partnerId)
            .collection('hubAgents')
            .doc(assistantData.id)
            .set(fullAssistant);

        console.log(`✅ Assistant created from template: ${assistantData.id}`);

        const serializedAssistant = serializeAssistant(fullAssistant, assistantData.id);

        return { success: true, assistantId: assistantData.id, assistant: serializedAssistant };
    } catch (error: any) {
        console.error('Create assistant from template error:', error);
        return { success: false, error: error.message };
    }
}

export async function updateAssistantAction(
    partnerId: string,
    assistantId: string,
    updates: Partial<Assistant>
): Promise<{ success: boolean; error?: string }> {
    try {
        if (!db) {
            return { success: false, error: 'Database unavailable' };
        }

        const safeUpdates = { ...updates };
        delete (safeUpdates as any).id;
        delete (safeUpdates as any).partnerId;
        delete (safeUpdates as any).createdAt;
        delete (safeUpdates as any).createdBy;

        await db
            .collection('partners')
            .doc(partnerId)
            .collection('hubAgents')
            .doc(assistantId)
            .update({
                ...safeUpdates,
                updatedAt: Timestamp.now(),
            });

        console.log(`✅ Assistant updated: ${assistantId}`);

        return { success: true };
    } catch (error: any) {
        console.error('Update assistant error:', error);
        return { success: false, error: error.message };
    }
}

export async function deleteAssistantAction(
    partnerId: string,
    assistantId: string
): Promise<{ success: boolean; error?: string }> {
    try {
        if (!db) {
            return { success: false, error: 'Database unavailable' };
        }

        if (assistantId.startsWith('essential-')) {
            return { success: false, error: 'Cannot delete essential assistants' };
        }

        await db
            .collection('partners')
            .doc(partnerId)
            .collection('hubAgents')
            .doc(assistantId)
            .delete();

        console.log(`✅ Assistant deleted: ${assistantId}`);

        return { success: true };
    } catch (error: any) {
        console.error('Delete assistant error:', error);
        return { success: false, error: error.message };
    }
}


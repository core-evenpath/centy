export async function duplicateAssistantAction(
    partnerId: string,
    assistantId: string,
    createdBy: string
): Promise<{ success: boolean; newAssistantId?: string; error?: string }> {
    try {
        if (!db) {
            return { success: false, error: 'Database unavailable' };
        }

        const docRef = await db
            .collection('partners')
            .doc(partnerId)
            .collection('hubAgents')
            .doc(assistantId)
            .get();

        if (!docRef.exists) {
            return { success: false, error: 'Assistant not found' };
        }

        const original = docRef.data() as Assistant;
        const newId = `custom-${Date.now()}-${Math.random().toString(36).substr(2, 9)}`;

        const duplicate: Assistant = {
            ...original,
            id: newId,
            name: `${original.name} (Copy)`,
            type: 'custom',
            createdAt: Timestamp.now() as any,
            updatedAt: Timestamp.now() as any,
            createdBy,
            usageCount: 0,
        };

        await db
            .collection('partners')
            .doc(partnerId)
            .collection('hubAgents')
            .doc(newId)
            .set(duplicate);

        return { success: true, newAssistantId: newId };
    } catch (error: any) {
        console.error('Duplicate assistant error:', error);
        return { success: false, error: error.message };
    }
}


export async function incrementAssistantUsageAction(
    partnerId: string,
    assistantId: string
): Promise<{ success: boolean }> {
    try {
        if (!db || !assistantId) {
            return { success: false };
        }

        const { FieldValue } = await import('firebase-admin/firestore');

        await db
            .collection('partners')
            .doc(partnerId)
            .collection('hubAgents')
            .doc(assistantId)
            .update({
                usageCount: FieldValue.increment(1),
            });

        return { success: true };
    } catch (error) {
        console.error('Increment usage error:', error);
        return { success: false };
    }
}

export async function getActiveAssistantsAction(
    partnerId: string
): Promise<{ success: boolean; assistants?: Array<{ id: string; name: string; avatar: string; color: string; type: string; description?: string }>; error?: string }> {
    try {
        if (!db) {
            return { success: false, error: 'Database unavailable' };
        }

        // Query agents that are explicitly stored in the partner's hubAgents collection and are active
        const snapshot = await db
            .collection('partners')
            .doc(partnerId)
            .collection('hubAgents')
            .where('isActive', '==', true)
            .get();

        const storedAssistants = snapshot.docs.map(doc => {
            const data = doc.data();
            return {
                id: doc.id,
                name: data.name || 'Unnamed',
                avatar: data.avatar || '🤖',
                color: data.color || 'blue',
                type: data.type || 'custom',
                description: data.description || '',
            };
        });

        // Get the IDs of stored assistants
        const storedIds = storedAssistants.map(a => a.id);

        // Add essential defaults that aren't already stored
        // This ensures partners always have access to essential agents in the inbox
        const missingEssentials = ESSENTIAL_ASSISTANT_DEFAULTS
            .filter(e => e.id && !storedIds.includes(e.id))
            .map(e => ({
                id: e.id!,
                name: e.name || 'Unnamed',
                avatar: e.avatar || '🤖',
                color: e.color || 'blue',
                type: e.type || 'essential',
                description: e.description || '',
            }));

        const allAssistants = [...storedAssistants, ...missingEssentials];

        return { success: true, assistants: allAssistants };
    } catch (error: any) {
        console.error('Get active assistants error:', error);
        return { success: false, error: error.message };
    }
}

export async function getAssistantByIdAction(
    partnerId: string,
    assistantId: string
): Promise<{ success: boolean; assistant?: Assistant; error?: string }> {
    try {
        if (!db) {
            return { success: false, error: 'Database unavailable' };
        }

        const docRef = await db
            .collection('partners')
            .doc(partnerId)
            .collection('hubAgents')
            .doc(assistantId)
            .get();

        if (!docRef.exists) {
            const essentialDefault = ESSENTIAL_ASSISTANT_DEFAULTS.find(e => e.id === assistantId);
            if (essentialDefault) {
                return {
                    success: true,
                    assistant: {
                        ...essentialDefault,
                        partnerId,
                        isActive: true,
                        createdAt: new Date(),
                        updatedAt: new Date(),
                        usageCount: 0,
                    } as Assistant,
                };
            }
            return { success: false, error: 'Assistant not found' };
        }

        return { success: true, assistant: serializeAssistant(docRef.data(), docRef.id) };
    } catch (error: any) {
        console.error('Get assistant by ID error:', error);
        return { success: false, error: error.message };
    }
}